package sefaz;

public class XmlLista {
	private String ultNSU;
	private String maxNSU;
	private String schema;
	private String nSU;
	private String sXML;
	
	
	public String getUltNSU() {
		return ultNSU;
	}
	public void setUltNSU(String ultNSU) {
		this.ultNSU = ultNSU;
	}
	public String getMaxNSU() {
		return maxNSU;
	}
	public void setMaxNSU(String maxNSU) {
		this.maxNSU = maxNSU;
	}
	public String getSchema() {
		return schema;
	}
	public void setSchema(String schema) {
		this.schema = schema;
	}
	public String getnSU() {
		return nSU;
	}
	public void setnSU(String nSU) {
		this.nSU = nSU;
	}
	public String getsXML() {
		return sXML;
	}
	public void setsXML(String sXML) {
		this.sXML = sXML;
	}
	
}
